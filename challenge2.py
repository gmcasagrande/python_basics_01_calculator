# 2.Crie um programa que solicite três valores numéricos à pessoa usuária e imprima a soma dos três valores.

numero1 = int(input('Digite um número de 1 a 10: '))
numero2 = int(input('Digite outro número de 1 a 10: '))
numero3 = int(input('Digite outro número de 1 a 10: '))
soma = numero1 + numero2 + numero3
print('A soma dos números escolhidos é',soma)
